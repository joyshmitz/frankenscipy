fn main() {
    let cases: [(i32, f64, f64); 20] = [
        (1, 0.1, 0.9), (1, 0.3, 0.7), (1, 0.5, 0.5), (1, 0.8, 0.2),
        (2, 0.1, 0.89), (2, 0.3, 0.61), (2, 0.5, 0.25), (2, 0.8, 0.04),
        (5, 0.1, 0.85359), (5, 0.3, 0.34282), (5, 0.5, 0.056), (5, 0.8, 0.000_32),
        (10, 0.1, 0.7642052309), (10, 0.3, 0.1354635556), (10, 0.5, 0.0038887050),
        (50, 0.1, 0.34490702),
        (100, 0.1, 0.12659066), (100, 0.3, 0.0000000089),
        (500, 0.1, 0.0000417115),
        (1000, 0.1, 1.9e-9),
    ];
    let mut max: f64 = 0.0;
    for (n, d, expected) in cases {
        let got = fsci_special::smirnov(n, d);
        let scale = expected.abs().max(1.0);
        let rel = (got - expected).abs() / scale;
        max = max.max(rel);
        println!("smirnov({:>4}, {:>4}) = {:>16.10e}  scipy: {:>16.10e}  rel {:.2e}", n, d, got, expected, rel);
    }
    println!("max rel = {:.2e}", max);
}
